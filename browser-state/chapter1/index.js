/**
 * Chapter 1: JavaScriptの値と参照
 *
 * このファイルではJavaScriptにおける値渡しと参照渡しの違いを示しています。
 * これはReduxやNgRxなどの状態管理において重要な概念です。
 *
 * なぜ重要なのか？
 * 状態管理ライブラリでは、アプリケーションの状態変更を正確に検知し、
 * UIを効率的に更新する必要があります。Reactなどのフレームワークは
 * オブジェクトの参照が変わったかどうかで再レンダリングを判断します。
 * もし状態を直接変更（ミューテート）すると、参照は同じままなので変更を
 * 検知できず、UIが更新されないという問題が発生します。
 *
 * そのため、ReduxやNgRxでは「イミュータブル（不変）」な方法で
 * 状態を更新することが基本原則となっています。つまり、状態を変更する
 * 際には必ず新しいオブジェクト参照を作成します。これにより変更検知が
 * 正しく機能し、アプリケーションが期待通りに動作するのです。
 */

// プリミティブ型の例
// プリミティブ型（数値、文字列、真偽値など）は値渡しされます
let a = 5;
let b = a; // 値のコピー - aの値がbにコピーされるだけで、両者に関連性はありません
a = 10; // aの値を変更しても
console.log(b); // 5（aの変更はbに影響しない）- bの値は変わりません

// オブジェクト型の例
// オブジェクト型（オブジェクト、配列など）は参照渡しされます
let obj1 = { count: 5 };
let obj2 = obj1; // 参照のコピー - obj1とobj2は同じオブジェクトを参照します
obj1.count = 10; // obj1の内容を変更すると
console.log(obj2.count); // 10（obj1の変更がobj2に影響する）- obj2も同じ変更が反映されます

// 注意: これがReduxやNgRxで不変性（イミュータビリティ）が重要な理由です
// 状態の変更を検知するには、参照の変更が必要になります
