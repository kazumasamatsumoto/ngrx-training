/**
 * Todo Model - データモデル定義
 * 
 * このインターフェースは、アプリケーション全体で使用されるTodoエンティティの
 * 構造を定義します。
 * 
 * NgRxチュートリアルにおける役割:
 * - 型安全なデータモデルの定義
 * - アプリケーション全体での一貫したデータ構造
 * - NgRx Entityで使用するためのIDプロパティの提供
 */
export interface Todo {
  /**
   * 一意の識別子
   * NgRx Entityで使用され、エンティティを一意に識別します。
   */
  id: number;
  
  /**
   * Todoのタイトル
   * ユーザーが入力したタスクの内容を表します。
   */
  title: string;
  
  /**
   * 完了状態
   * Todoが完了したかどうかを示すフラグです。
   */
  completed: boolean;
}
